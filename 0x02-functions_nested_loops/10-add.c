#include"main.h"

/**
 * add - Adds two integers and returns the result.
 *
 * This function takes two integers as arguments and returns their sum as an
 * integer. It uses the + operator to add the two integers, and returns the
 * result using the return statement.
 *
 * @a: The first integer to add.
 * @b: The second integer to add.
 *
 * Return: The sum of a and b.
 */
int add(int a, int b)
{
	int sum;

	sum = a + b;
	return (sum);
}
